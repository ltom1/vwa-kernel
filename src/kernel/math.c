#include <types.h>
#include <math.h>


///////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief  Calculates the nth power of a value.
///
/// @param  value   The value.
/// @param  n       The power.
///
/// @returns    value ^ n.
///////////////////////////////////////////////////////////////////////////////////////////////////

u64 math_pow(u64 value, u64 n) {

    u64 res = 1;
    for (u64 i = 0; i < n; i++) {
        res = res * value;
    }

    return res;
}
